function CodeViewer({ code, lang = 'typescript' }) {
  const [html, setHtml] = useState('')

  useEffect(() => {
    getHighlighter().then(highlighter => {
      const result = highlighter.codeToHtml(code, {
        lang,
        theme: 'github-light',
      })
      setHtml(result)
    })
  }, [code, lang])

  if (!html) {
    return (
      <div className="flex items-center justify-center h-full text-[var(--color-muted)]">
        <PixelIcon name="loader" size={20} className="animate-spin" />
      </div>
    )
  }

  const lineCount = code.split('\n').length

  return (
    <div className="overflow-auto h-full flex" style={{ tabSize: 2 }}>
      {/* Line numbers */}
      <div className="flex-shrink-0 py-6 pl-4 pr-2 text-right select-none">
        {Array.from({ length: lineCount }, (_, i) => (
          <div key={i} className="text-[13px] leading-relaxed font-mono text-[var(--color-muted)]/40">{i + 1}</div>
        ))}
      </div>
      {/* Code */}
      <div
        className="flex-1 text-[13px] leading-relaxed [&_pre]:py-6 [&_pre]:pr-6 [&_pre]:pl-2 [&_pre]:m-0 [&_code]:font-mono"
        dangerouslySetInnerHTML={{ __html: html }}
      />
    </div>
  )
}

