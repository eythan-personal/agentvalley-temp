export function ReviewSheet({ open, onClose, file, type = 'code' }) {
  const [phase, setPhase] = useState(0) // 0=closed, 1=open
  const [commentText, setCommentText] = useState('')
  const [comments, setComments] = useState(MOCK_COMMENTS)
  const [vote, setVote] = useState(null) // 'up' | 'down' | null
  const overlayRef = useRef(null)

  useEffect(() => {
    if (open) {
      requestAnimationFrame(() => requestAnimationFrame(() => setPhase(1)))
      document.body.style.overflow = 'hidden'
    } else {
      setPhase(0)
    }
    return () => { document.body.style.overflow = '' }
  }, [open])

  const handleClose = () => {
    setPhase(0)
    setTimeout(onClose, 350)
  }

  useEffect(() => {
    if (!open) return
    const handler = (e) => { if (e.key === 'Escape') handleClose() }
    document.addEventListener('keydown', handler)
    return () => document.removeEventListener('keydown', handler)
  }, [open])

  const handleComment = () => {
    if (!commentText.trim()) return
    setComments(prev => [...prev, { agent: 'You', time: 'Just now', text: commentText.trim() }])
    setCommentText('')
  }

  if (!open && phase === 0) return null

  const filePath = file?.name || (type === 'markdown' ? 'reports/weekly-analysis.md' : 'src/services/payment.ts')
  const objective = file?.objective || (type === 'markdown' ? 'Weekly competitor pricing analysis' : 'Payment service security review')

  return (
    <div
      ref={overlayRef}
      className="fixed inset-0 z-[9998]"
      onClick={handleClose}
    >
      {/* Backdrop */}
      <div
        className="absolute inset-0 bg-black/50"
        style={{
          opacity: phase === 1 ? 1 : 0,
          backdropFilter: phase === 1 ? 'blur(8px)' : 'blur(0px)',
          WebkitBackdropFilter: phase === 1 ? 'blur(8px)' : 'blur(0px)',
          transition: 'opacity 0.3s ease-out, backdrop-filter 0.3s ease-out',
        }}
      />

      {/* Sheet */}
      <div
        className="absolute bottom-0 left-0 right-0 bg-[var(--color-surface)] rounded-t-2xl flex flex-col"
        onClick={e => e.stopPropagation()}
        style={{
          height: '95vh',
          transform: phase === 1 ? 'translateY(0)' : 'translateY(100%)',
          transition: phase === 1
            ? 'transform 0.4s cubic-bezier(0.16, 1, 0.3, 1)'
            : 'transform 0.3s cubic-bezier(0.4, 0, 1, 1)',
          boxShadow: '0 -10px 40px rgba(0,0,0,0.15)',
        }}
      >
        {/* Header */}
        <div className="flex items-center justify-between px-6 py-3 border-b border-[var(--color-border)]">
          <div className="flex items-center gap-3 min-w-0">
            <div className="text-[13px] font-bold text-[var(--color-heading)] truncate" style={{ fontFamily: 'var(--font-display)' }}>{objective}</div>
            <span className="text-[var(--color-muted)]">/</span>
            <div className="text-[12px] font-mono text-[var(--color-muted)] truncate flex-shrink-0">{filePath}</div>
          </div>
          <div className="flex items-center gap-2 flex-shrink-0">
            {/* Thumbs up/down */}
            <button
              type="button"
              onClick={() => setVote(vote === 'up' ? null : 'up')}
              className={`w-8 h-8 flex items-center justify-center rounded-lg cursor-pointer transition-colors ${vote === 'up' ? 'bg-[var(--color-accent)]/15 text-[var(--color-accent)]' : 'text-[var(--color-muted)] hover:text-[var(--color-heading)] hover:bg-[var(--color-bg-alt)]'}`}
            >
              <PixelIcon name="thumbs-up" size={16} aria-hidden="true" />
            </button>
            <button
              type="button"
              onClick={() => setVote(vote === 'down' ? null : 'down')}
              className={`w-8 h-8 flex items-center justify-center rounded-lg cursor-pointer transition-colors ${vote === 'down' ? 'bg-red-500/15 text-red-500' : 'text-[var(--color-muted)] hover:text-[var(--color-heading)] hover:bg-[var(--color-bg-alt)]'}`}
            >
              <PixelIcon name="thumbs-down" size={16} aria-hidden="true" />
            </button>
            <div className="w-px h-5 bg-[var(--color-border)] mx-1" />
            <button
              type="button"
              onClick={handleClose}
              className="w-8 h-8 flex items-center justify-center rounded-lg text-[var(--color-muted)] hover:text-[var(--color-heading)] hover:bg-[var(--color-bg-alt)] cursor-pointer transition-colors"
            >
              <PixelIcon name="close" size={16} aria-hidden="true" />
            </button>
          </div>
        </div>

        {/* Body — code + sidebar */}
        <div className="flex flex-1 overflow-hidden">
          {/* Content area — 80% */}
          <div className="flex-[4] overflow-auto border-r border-[var(--color-border)] bg-[var(--color-bg)]">
            {type === 'markdown' ? (
              <MarkdownViewer content={MOCK_MARKDOWN} />
            ) : (
              <CodeViewer code={MOCK_CODE} />
            )}
          </div>

          {/* Sidebar — 20% */}
          <div className="flex-[1] flex flex-col min-w-[280px] max-w-[360px]">
            {/* Task details */}
            <div className="px-4 py-4 border-b border-[var(--color-border)]">
              <div className="text-[10px] font-mono uppercase tracking-wider text-[var(--color-muted)] mb-2">Task Details</div>
              <div className="space-y-1.5 text-[11px]">
                <div className="flex justify-between">
                  <span className="text-[var(--color-muted)]">Task</span>
                  <span className="text-[var(--color-heading)] font-medium font-mono">TSK-038</span>
                </div>
                <div className="flex justify-between">
                  <span className="text-[var(--color-muted)]">Agent</span>
                  <span className="text-[var(--color-heading)] font-medium flex items-center gap-1.5">
                    <AgentDot name="Cipher" size={16} />
                    Cipher
                  </span>
                </div>
                <div className="flex justify-between">
                  <span className="text-[var(--color-muted)]">Status</span>
                  <span className="text-[var(--color-heading)] font-medium">Needs Review</span>
                </div>
                <div className="flex justify-between">
                  <span className="text-[var(--color-muted)]">Files Changed</span>
                  <span className="text-[var(--color-heading)] font-medium tabular-nums">3</span>
                </div>
                <div className="flex justify-between">
                  <span className="text-[var(--color-muted)]">Additions</span>
                  <span className="text-[var(--color-accent)] font-medium tabular-nums">+47</span>
                </div>
              </div>
            </div>

            {/* Comments */}
            <div className="flex-1 flex flex-col overflow-hidden">
              <div className="px-4 py-3">
                <div className="text-[10px] font-mono uppercase tracking-wider text-[var(--color-muted)]">Comments ({comments.length})</div>
              </div>
              <div className="flex-1 overflow-auto px-4 space-y-4">
                {comments.map((comment, i) => (
                  <div key={i} className="flex gap-2.5">
                    <AgentDot name={comment.agent} size={24} className="flex-shrink-0 mt-0.5" />
                    <div>
                      <div className="flex items-center gap-2 mb-0.5">
                        <span className="text-[12px] font-semibold text-[var(--color-heading)]">{comment.agent}</span>
                        <span className="text-[10px] text-[var(--color-muted)]">{comment.time}</span>
                      </div>
                      <p className="text-[12px] text-[var(--color-body)] leading-relaxed">{comment.text}</p>
                    </div>
                  </div>
                ))}
              </div>

              {/* Comment input */}
              <div className="px-4 py-3 border-t border-[var(--color-border)]">
                <div className="flex gap-2">
                  <input
                    type="text"
                    value={commentText}
                    onChange={e => setCommentText(e.target.value)}
                    onKeyDown={e => { if (e.key === 'Enter') handleComment() }}
                    placeholder="Add a comment..."
                    className="flex-1 h-8 px-3 rounded-lg bg-[var(--color-bg-alt)] text-[12px] text-[var(--color-heading)] placeholder:text-[var(--color-muted)] border-none outline-none focus:ring-1 focus:ring-[var(--color-accent)]"
                  />
                  <button
                    type="button"
                    onClick={handleComment}
                    disabled={!commentText.trim()}
                    className="h-8 px-3 rounded-lg bg-[var(--color-accent)] text-[#0d2000] text-[12px] font-semibold cursor-pointer transition-[opacity,scale] duration-150 active:scale-[0.96] disabled:opacity-40 disabled:cursor-default"
                  >
                    Send
                  </button>
                </div>
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  )
}
