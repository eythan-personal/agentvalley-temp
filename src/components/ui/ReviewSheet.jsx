const MOCK_CODE = `import { useState, useEffect } from 'react'

interface PaymentConfig {
  provider: 'stripe' | 'paypal'
  currency: string
  webhookSecret: string
}

export class PaymentService {
  private config: PaymentConfig
  private retryCount = 3

  constructor(config: PaymentConfig) {
    this.config = config
  }

  async processPayment(amount: number, customerId: string) {
    const session = await this.createSession({
      amount,
      currency: this.config.currency,
      customer: customerId,
      metadata: {
        timestamp: Date.now(),
        provider: this.config.provider,
      },
    })

    // Verify webhook signature before processing
    if (!this.verifySignature(session.signature)) {
      throw new PaymentError('Invalid webhook signature', {
        sessionId: session.id,
        provider: this.config.provider,
      })
    }

    for (let attempt = 0; attempt < this.retryCount; attempt++) {
      try {
        const result = await this.executeCharge(session)
        await this.notifyWebhook(result)
        return { success: true, transactionId: result.id }
      } catch (err) {
        if (attempt === this.retryCount - 1) throw err
        await this.delay(Math.pow(2, attempt) * 1000)
      }
    }
  }

  private async createSession(params: Record<string, unknown>) {
    const response = await fetch('/api/payments/session', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(params),
    })
    if (!response.ok) throw new Error('Session creation failed')
    return response.json()
  }

  private verifySignature(signature: string): boolean {
    const expected = crypto
      .createHmac('sha256', this.config.webhookSecret)
      .update(signature)
      .digest('hex')
    return crypto.timingSafeEqual(
      Buffer.from(signature),
      Buffer.from(expected)
    )
  }

  private async executeCharge(session: any) {
    return fetch('/api/payments/charge', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ sessionId: session.id }),
    }).then(r => r.json())
  }

  private async notifyWebhook(result: any) {
    await fetch(this.config.webhookUrl, {
      method: 'POST',
      body: JSON.stringify(result),
    })
  }

  private delay(ms: number) {
    return new Promise(resolve => setTimeout(resolve, ms))
  }
}`

